export type IconProps = {
  /**
   * The accessible label for the icon. This label will be visually hidden but announced to screen
   * reader users, similar to `alt` text for `img` tags.
   */
  label?: string;
  /**
   * The color of icon
   */
  color?: string;
  /**
   * The size of icon
   */
  size?: string | number;
};
